const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const preferences = usePtHubRegionalPreferences();
  const language = preferences.language;
  const region = preferences.region;
  const dir = RTL_LANGUAGES.has(language) ? "rtl" : "ltr";
  const locale = `${language}-${REGION_LOCALE_MAP[region]}`;

  useEffect(() => {
    document.documentElement.lang = language;
    document.documentElement.dir = dir;
    document.documentElement.dataset.locale = locale;
  }, [dir, language, locale]);

  const value = useMemo<I18nContextValue>(
    () => ({
      dir,
      language,
      locale,
      region,
      setLanguage: (nextLanguage) =>
        writePtHubRegionalPreferences({ language: nextLanguage }),
      setRegion: (nextRegion) =>
        writePtHubRegionalPreferences({ region: nextRegion }),
      t: (key, fallback) => translations[language][key] ?? fallback ?? key,
    }),
    [dir, language, locale, region],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used within I18nProvider");
  }
  return context;
}
